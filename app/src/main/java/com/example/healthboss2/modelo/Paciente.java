package com.example.healthboss2.modelo;

public class Paciente {
    private Integer idPaciente;
    private String nomePaciente;
    private String cpfPaciente;
    private String nascimentoPaciente;
    private String telefonePaciente;
    private String generoPaciente;
    private String senhaPaciente;
    private String idPlanodeSaude;

    public Integer getIdPaciente() {
        return idPaciente;
    }

    public void setIdPaciente(Integer idPaciente) {
        this.idPaciente = idPaciente;
    }

    public String getNomePaciente() {
        return nomePaciente;
    }

    public void setNomePaciente(String nomePaciente) {
        this.nomePaciente = nomePaciente;
    }

    public String getCpfPaciente() {
        return cpfPaciente;
    }

    public void setCpfPaciente(String cpfPaciente) {
        this.cpfPaciente = cpfPaciente;
    }

    public String getNascimentoPaciente() {
        return nascimentoPaciente;
    }

    public void setNascimentoPaciente(String nascimentoPaciente) {
        this.nascimentoPaciente = nascimentoPaciente;
    }

    public String getTelefonePaciente() {
        return telefonePaciente;
    }

    public void setTelefonePaciente(String telefonePaciente) {
        this.telefonePaciente = telefonePaciente;
    }

    public String getGeneroPaciente() {
        return generoPaciente;
    }

    public void setGeneroPaciente(String generoPaciente) {
        this.generoPaciente = generoPaciente;
    }

    public String getSenhaPaciente() {
        return senhaPaciente;
    }

    public void setSenhaPaciente(String senhaPaciente) {
        this.senhaPaciente = senhaPaciente;
    }

    public String getIdPlanodeSaude() {
        return idPlanodeSaude;
    }

    public void setIdPlanodeSaude(String idPlanodeSaude) {
        this.idPlanodeSaude = idPlanodeSaude;
    }

}
